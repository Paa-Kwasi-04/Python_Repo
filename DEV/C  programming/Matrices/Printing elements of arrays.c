#include <stdio.h>

int main() {

    //Another way of defining a 2D array
    int a[2][2] = {1,2,4,6};

    for(int j  =0;j<2;j++){
        printf("\n");
        for(int i = 0;i<2;i++){
            printf("%d ",a[j][i]);
        }
    }



    return 0;
}


























